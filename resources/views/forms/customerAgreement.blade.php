<h4><strong>Interpretation</strong></h4>
<ul class="no_bullet">
    <li><p> a) <strong>Customer</strong> - Any Person who purchase or sells or otherwise acquires or disposes of any product or any service therein or an interest therein through NSB Fund Management Co. Ltd (NSBFMC) or who negotiated with NSBFMC for the possible acquisition or disposition of such product or any service therein or interest and shall include where the context so permits a legal representative (a person who is acting for a customer under a power of attorney) of such customer or of the state of such customer</p>
    </li>
    <li>
        <p>  b) <strong>	Purpose of CAGS </strong> - 
            This CAGS covers the responsibilities, duties and legal liabilities between NSBFMC and the customer (Eligible investor) during the contract period of the securities investment in line with the schedule 1 of the Local Treasury Bills Ordinance, No 8 of 1923.
        </p>
    </li>
    <li>
        <p>  c) 
            <strong>
                Treasury Bills/Bonds
            </strong>
            -A negotiable debt obligation/instruments backed by the Government of Sri Lanka under the Local Treasury Bills Ordinance No. 08 of 1923 and the Registered Stock and Securities Ordinance No.7 of 1937 (as amended). As an Agent of the Government of Sri Lanka, the Public Debt Department of the Central Bank of Sri Lanka'(CBSL) issues the Treasury bills and Treasury bonds.
        </p>
    </li>
    <li>
        <p> d)<strong> Eligible Investors for Treasury Bills and Bonds </strong></p>
        <ul class="bullet"> 
            <li>
               Sri Lankan Citizens 
            </li>
            <li>
               Corporate bodies incorporated in Sri Lanka 
             </li>
             <li>
              Foreign country funds, mutual funds and regional funds 
             </li>
             <li>
                Corporate bodies incorporated outside Sri Lanka 
             </li>
             <li>
               Citizens of foreign states and investor categories that time to time decided by the Central 
                Bank of Sri Lanka
             </li>
        </ul>
        <li>
            <p>
                e)  <strong> Trade Date</strong> - The trade date is when an order to purchase, sell or otherwise acquire a security is performed. The date in which an order is executed in the market
            </p>
        </li>
        <li>
            <p>
                f) <strong> Settlement Date / Value Date </strong> -The date on which an executed security trade must be settled by the customer and NSBFMC. That is, the date on which the buyer must pay for the scripless securities delivered by the seller.
            </p>
        </li>
        <li>
            <p>
                g) <strong>Scripless Securities  </strong>
                -The scripless government securities are the securities issued in data entry form without a paper certificate. Initially, Treasury Bills and Treasury Bonds are issued in scripless form. To enable primary issues and recording of secondary market transactions of scripless securities, a computer based central depository and a settlement system have been installed. The transactions will be settled through an electronic settlement arrangement known as the Scripless Securities Settlement System (SSSS). The SSSS and the Central Depository System (CDS) are named as 'LankaSecure'
            </p>
        </li>
         
          <li>
              <p> h)
                  <strong>
                  	Securities Account 
                  </strong>
                  -Means an account maintained in LankaSecure by a direct participant to record title and interests in Scripless treasury bills and bonds of such direct participant and if such direct participant is a dealer direct participant, of its customers
              </p>
          </li>
    </li>

</ul>

