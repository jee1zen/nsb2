<h5>
    General Principles
</h5>
<ul class="no_bullet">
    <li> NSB Fund Management Co Ltd will ensure that in all its dealing with Customers and within the context of its authorization it:
        <ul class="alpha_bullet">
            <li>
                acts honestly, fairly and professionally in the best interests of its customers and the integrity of the market
            </li>
            <li>
               acts with due skill, care and diligence in the best interests of its customers
            </li>
            <li>
              does not recklessly, negligently or deliberately mislead a customer as to the real or perceived advantages or disadvantages of any investment
            </li>
            <li>
               seeks from its customers information relevant to the investment 
            </li>
            <li>
                makes full disclosure of all relevant material information, including all charges, in a way that seeks to inform the customer
            </li>
            <li>
               seeks to avoid conflicts of interest
            </li>
            <li>
                corrects errors and handles complaints speedily, efficiently and fairly
            </li>
            <li>
               	does not exert undue pressure or undue influence on a customer
            </li>
            <li>
               ensure that the name of any investment is not misleading in terms of the benefits that the investment can deliver to a customer
            </li>
            <li>
               ensure that all instructions from a customer are processed properly and promptly
            </li>
            <li>
               ensure that any funds received by it in favor of a customer, are credited to that account by close of the business day on which the funds are received
            </li>
            <li>
               where NSB FMC deals with a person who is acting for a customer under a power of attorney NSB FMC will;
               <ul>
                   <li> Obtain a certified copy of the power of attorney</li> 
                   <li>Ensure that the power of attorney allows the person to act on the customer’s behalf and operate within the limitations set out in the power of attorney.</li>
               </ul>
            </li>
        </ul>
    </li>
</ul>
<h5>Provision of Information</h5>
<ul class="alpha_bullet">
    <li>
      If NSB FMC ceases operations, merges with another, or transfers all or part of its activities to another Primary Dealer, NSB FMC will;
      <ul>
          <li>Provide at least two months’ notice to its customers to enable them to make alternative arrangements</li>
          <li>Ensure all outstanding transactions are properly completed or inform the customer of how continuity of services will be provided following the transfer or merger</li>
      </ul>
    </li>
    <li>
        NSB FMC ensures that where it communicates with a Customer using electronic media, it has in place appropriate arrangements to ensure the security of information received from the Customer and the secure transmission of information to the Customer
    </li>

</ul>
<h5>A Customer’s obligations toward a Primary Dealer</h5>
<p>
    It is imperative that the Customer informs NSB FMC at all times of any changes to his/her address and contact details to facilitate to contact the Customer at any time and to ensure that Customer registration details in the CDS are current and accurate
</p>
<ul class="alpha_bullet">
    <li>The Customer should have the complete understanding of the product/service offered by the Primary Dealer before entering into the contract.</li>
    <li>The Customer should duly fill and submit the required application forms and supporting documents in time</li>
</ul>
